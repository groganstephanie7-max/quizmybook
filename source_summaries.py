def show_source_summary(topic_key):
    topic_key = topic_key.lower().strip()

    if topic_key not in source_summaries:
        print("\nI do not have a source-backed summary for that topic yet.")
        print("Current source summary topics:")
        for topic in source_summaries:
            print("-", topic)
        return

    summary_data = source_summaries[topic_key]

    print("\n" + "=" * 40)
    print("Source-Backed Topic Summary")
    print("=" * 40)

    print("\nTopic:")
    print(summary_data["topic"])

    print("\nSummary:")
    print(summary_data["summary"])

    print("\nKey Terms:")
    for term in summary_data["key_terms"]:
        print("-", term)

    print("\nSource Notes:")
    for note in summary_data["source_notes"]:
        print("-", note)

    if "source_links" in summary_data:
        print("\nSource Links:")
        for source in summary_data["source_links"]:
            print("-", source["name"])
            print("  URL:", source["url"])
            print("  Note:", source["note"])

    print("\nBeginner Lesson Plan:")
    for index, step in enumerate(summary_data["lesson_plan"], start=1):
        print(str(index) + ".", step)